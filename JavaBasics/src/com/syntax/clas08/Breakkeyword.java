package com.syntax.clas08;

public class Breakkeyword {

	public static void main(String[] args) {

		for (int i=1; i<4; i++) {
			
			System.out.println("I am coding inside of the loop");
			
			break;
		}
		System.out.println("End of the code");
		
		boolean winter=true;
		while(winter) {
			System.out.println("It is cold");
			
			break;
		}
	}

}
 