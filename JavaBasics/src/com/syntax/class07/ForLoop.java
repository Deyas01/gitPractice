public class ForLoop {

	public static void main(String[] args) {
for (int i=1; i<=5; i++) {
	System.out.println("hello");
}
System.out.println("I need to print numbers form 1 to 20");
for(int i=1; i<=20; i++) {
	System.out.println(i+" ");
}
System.out.println("I need to print numbers form 10 to 25");
for (int i=10; i<=25; i++) {
	System.out.println(i+" ");
}
System.out.println("I need to print numbers form 50 to 1");
for (int i=50; i>=1; i--) {
	System.out.println(i+" ");
}
System.out.println("_________________________________");
for (int i=0; i<=12; i++){
	 
    System.out.println(i);
	}
}
}
